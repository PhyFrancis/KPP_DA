function y = combine_type1(type1_data) 
	% type1_data is a cell(48,1)
	% y is a cell(10,1)
	
	y = cell(10,1);

	nConf = size(type1_data,2) / 48;

	y{1,1} = 1.0 / (6^0.5) * (...
		(-1) * type1_data(:,map_C(1,1,0,'VA')+[0:nConf-1]*48) +...
		(+1) * type1_data(:,map_C(1,1,0,'AV')+[0:nConf-1]*48) +...
		(-1) * type1_data(:,map_C(4,0,1,'VA')+[0:nConf-1]*48) +...
		(+1) * type1_data(:,map_C(4,0,1,'AV')+[0:nConf-1]*48) +...
		(+2) * type1_data(:,map_C(4,0,0,'VA')+[0:nConf-1]*48) +...
		(+2) * type1_data(:,map_C(4,0,0,'AV')+[0:nConf-1]*48) ); 

	y{2,1} = 1.0 / (6^0.5) * (...
		(-1) * type1_data(:,map_C(2,1,0,'VA')+[0:nConf-1]*48) +...
		(+1) * type1_data(:,map_C(2,1,0,'AV')+[0:nConf-1]*48) +...
		(-1) * type1_data(:,map_C(5,0,1,'VA')+[0:nConf-1]*48) +...
		(+1) * type1_data(:,map_C(5,0,1,'AV')+[0:nConf-1]*48) +...
		(+2) * type1_data(:,map_C(6,0,0,'VA')+[0:nConf-1]*48) +...
		(+2) * type1_data(:,map_C(6,0,0,'AV')+[0:nConf-1]*48) );

	y{3,1} = 1.0 / (6^0.5) * (...
		(-3) * type1_data(:,map_C(4,0,1,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(4,0,1,'AV')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(4,0,0,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(4,0,0,'AV')+[0:nConf-1]*48) );

	y{4,1} = 1.0 / (6^0.5) * (...
		(-1) * type1_data(:,map_C(3,0,0,'VA')+[0:nConf-1]*48) +...
		(-1) * type1_data(:,map_C(3,0,0,'AV')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(6,0,0,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(6,0,0,'AV')+[0:nConf-1]*48) +...
		(-1) * type1_data(:,map_C(2,1,0,'VA')+[0:nConf-1]*48) +...
		(+1) * type1_data(:,map_C(2,1,0,'AV')+[0:nConf-1]*48) +...
		(-3) * type1_data(:,map_C(5,0,1,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(5,0,1,'AV')+[0:nConf-1]*48) );

	y{5,1} = 1.0 / (6^0.5) * (...
		(+3) * type1_data(:,map_C(4,0,1,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(4,0,1,'AV')+[0:nConf-1]*48) +...
		(-3) * type1_data(:,map_C(4,0,0,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(4,0,0,'AV')+[0:nConf-1]*48) );

	y{6,1} = 1.0 / (6^0.5) * (...
		(-1) * type1_data(:,map_C(3,0,0,'VA')+[0:nConf-1]*48) +...
		(+1) * type1_data(:,map_C(3,0,0,'AV')+[0:nConf-1]*48) +...
		(-3) * type1_data(:,map_C(6,0,0,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(6,0,0,'AV')+[0:nConf-1]*48) +...
		(-1) * type1_data(:,map_C(2,1,0,'VA')+[0:nConf-1]*48) +...
		(-1) * type1_data(:,map_C(2,1,0,'AV')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(5,0,1,'VA')+[0:nConf-1]*48) +...
		(+3) * type1_data(:,map_C(5,0,1,'AV')+[0:nConf-1]*48) );

	y{7,1} = 1.0 / (6^0.5) * (...
		(-1)   * type1_data(:,map_C(1,1,0,'VA')+[0:nConf-1]*48) +...
		(-1)   * type1_data(:,map_C(1,1,0,'AV')+[0:nConf-1]*48) +...
		(+1/2) * type1_data(:,map_C(1,0,0,'VA')+[0:nConf-1]*48) +...
		(-1/2) * type1_data(:,map_C(1,0,0,'AV')+[0:nConf-1]*48) +...
		(-3/2) * type1_data(:,map_C(4,0,0,'VA')+[0:nConf-1]*48) +...
		(+3/2) * type1_data(:,map_C(4,0,0,'AV')+[0:nConf-1]*48) );

	y{8,1} = 1.0 / (6^0.5) * (...
		(+1/2) * type1_data(:,map_C(3,0,0,'VA')+[0:nConf-1]*48) +...
		(-1/2) * type1_data(:,map_C(3,0,0,'AV')+[0:nConf-1]*48) +...
		(-3/2) * type1_data(:,map_C(6,0,0,'VA')+[0:nConf-1]*48) +...
		(+3/2) * type1_data(:,map_C(6,0,0,'AV')+[0:nConf-1]*48) +...
		(-1)   * type1_data(:,map_C(2,1,0,'VA')+[0:nConf-1]*48) +...
		(-1)   * type1_data(:,map_C(2,1,0,'AV')+[0:nConf-1]*48) );

	y{9,1} = 1.0 / (6^0.5) * (...
		(-1)   * type1_data(:,map_C(1,1,0,'VA')+[0:nConf-1]*48) +...
		(+1)   * type1_data(:,map_C(1,1,0,'AV')+[0:nConf-1]*48) +...
		(+1/2) * type1_data(:,map_C(1,0,0,'VA')+[0:nConf-1]*48) +...
		(+1/2) * type1_data(:,map_C(1,0,0,'AV')+[0:nConf-1]*48) +...
		(+3/2) * type1_data(:,map_C(4,0,0,'VA')+[0:nConf-1]*48) +...
		(+3/2) * type1_data(:,map_C(4,0,0,'AV')+[0:nConf-1]*48) );

	y{10,1} = 1.0 / (6^0.5) * (...
		(+1/2) * type1_data(:,map_C(3,0,0,'VA')+[0:nConf-1]*48) +...
		(+1/2) * type1_data(:,map_C(3,0,0,'AV')+[0:nConf-1]*48) +...
		(+3/2) * type1_data(:,map_C(6,0,0,'VA')+[0:nConf-1]*48) +...
		(+3/2) * type1_data(:,map_C(6,0,0,'AV')+[0:nConf-1]*48) +...
		(-1)   * type1_data(:,map_C(2,1,0,'VA')+[0:nConf-1]*48) +...
		(+1)   * type1_data(:,map_C(2,1,0,'AV')+[0:nConf-1]*48) );
end
